	int ans = 1;
	int K = 100000;
	if (l != -1) {
		ans = K * (l + 1);
		cur = precalc[l];
	}
	while (cur <= n) {
		if (cur == n) {
			cout << ans << '\n';
			return;
		}
		int cp = cur;
		int mx = 0;
		while (cp) {
			mx = max(mx, cp % 10);
			cp /= 10;
		}
		cur += mx;
		ans++;
	}
